%define ATA0 0x1f0
%define ATA1 0x1f1
%define ATA2 0x1f2
%define ATA3 0x1f3
%define ATA4 0x1f4
%define ATA5 0x1f5
%define ATA6 0x1f6
%define ATA7 0x1f7

	cpu     186

start:

;;;; SETUP INTERRUPT HANDLERS ;;;;

	cli
	xor     di, di
	mov     es, di
	mov     cx, 0x1f
blankints:
	mov     ax, blank
	stosw
	mov     ax, cs
	stosw
	loop	blankints
	mov     word [es:0x08*4], INT08 ; TIMER HANDLER
	mov     word [es:0x09*4], INT09 ; KEYBOARD HANDLER
	mov     word [es:0x10*4], INT10 ; VIDEO SERVICE
	mov     word [es:0x12*4], INT12 ; MEMORY SERVICE
	mov     word [es:0x13*4], INT13 ; DISK SERVICE
	mov     word [es:0x16*4], INT16 ; KEYBOARD SERVICE
	mov     word [es:0x1a*4], INT1A ; TIME SERVICE

;;;; BIOS DATA AREA ;;;;

	mov		di, 0x40
	mov		es, di
	mov		word [es:0x1a], 0x1e
	mov		word [es:0x1c], 0x1e
	
;;;; IBM COMPATIBLE STACK ;;;;

	mov		sp, 0x30
	mov		ss, sp
	mov		sp, 0x0100
	
;;;; INITIALIZE 8259 PROGRAMMABLE INTERRUPT CONTROLLER ;;;;

	mov		al, 16
	out		0x20, al
	mov		al, 8
	out		0x21, al
	xor		al, al	
	out		0x21, al
	xor		al, al
	out		0x21, al
	
;;;; INITIALIZE 8253 TIMER ;;;;

	mov		al, 0x36
	out		0x43, al
	xor		al, al
	out		0x40, al
	out		0x40, al
	
;;;; INITIALIZE 8042 KEYBOARD CONTROLLER ;;;;

	mov		al, 0x60
	out		0x64, al
	mov		al, 0x61
	out		0x60, al
	
;;;; BIOS DATA AREA ;;;;

	mov		di, 0x40
	mov		es, di
	mov		word [es:0x50], 0  ; cursor position

;;;; INITIALIZE VIDEO TEXT MODE ;;;;

	mov		ax, 3
	int		0x10

;;;; LOAD BOOT SECTOR ;;;;
	
	xor		ax, ax
	mov		es, ax
	mov		ds, ax
	mov     ax, 0x0201
	mov     bx, 0x7c00
	mov     cx, 1
	mov     dx, 0x0080
	int     0x13
	
	jmp     0:0x7c00

;;;; TIMER INTERRUPT HANDLER ;;;;

INT08:
	push	ds
	push	ax
	mov		ax, 0x40
	mov		ds, ax
	clc
	adc		word [ds:0x6c], 1
	adc		word [ds:0x6e], 0
	int		0x1c
	mov		al, 0x20
	out		0x20, al
	pop		ax
	pop		ds
	iret

;;;; KEYBOARD INTERRUPT HANDLER ;;;;

INT09:
	push	ax
	push	bx
	push	es
	push	di
	push	ds
	in      al, 0x60
	mov		di, 0x40
	mov		es, di		
	mov		di, [es:0x1c]
	cmp		al, 0x1d                   ; <CTRL> down
	jne		INT09_not_1D
	or      [es:0x17], byte 00000100b	
	jmp		INT09_nobuff
INT09_not_1D:
	cmp		al, 0x9d                   ; <CTRL> up
	jne		INT09_not_9D
	and     [es:0x17], byte 11111011b	
	jmp		INT09_nobuff
INT09_not_9D:
	cmp		al, 0x38                   ; <ALT> down
	jne		INT09_not_38
	or      [es:0x17], byte 00001000b	
	jmp		INT09_nobuff
INT09_not_38:
	cmp		al, 0xb8                   ; <ALT> up
	jne		INT09_not_B8
	and     [es:0x17], byte 11110111b
	jmp		INT09_nobuff
INT09_not_B8:
	cmp		al, 0x2a                   ; <LSHIFT> down
	jne		INT09_not_2A
	or      [es:0x17], byte 00000010b
	jmp		INT09_nobuff
INT09_not_2A:
	cmp		al, 0xaa                   ; <LSHIFT> up
	jne		INT09_not_AA
	and     [es:0x17], byte 11111101b
	jmp		INT09_nobuff
INT09_not_AA:
	cmp		al, 0x36                   ; <RSHIFT> down
	jne		INT09_not_36
	or      [es:0x17], byte 00000001b	
	jmp		INT09_nobuff
INT09_not_36:
	cmp		al, 0xb6                   ; <RSHIFT> up
	jne		INT09_not_B6
	and     [es:0x17], byte 11111110b	
	jmp		INT09_nobuff
INT09_not_B6:
	cmp		al, 0x7f
	ja		INT09_nobuff
	mov		bx, cs
	mov		ds, bx
	mov		ah, al
	mov		bx, ASCII
	xlatb
	mov		bl, [es:0x17]
	test	bl, 3
	jz		INT09_not_SHIFT
	sub		al, 0x20
INT09_not_SHIFT:
	mov		bl, [es:0x17]
	test	bl, 4
	jz		INT09_not_CTRL
	sub		al, 0x60
INT09_not_CTRL:
	mov		bl, [es:0x17]
	test	bl, 8
	jz		INT09_not_ALT
	xor		al, al
INT09_not_ALT:
	mov		word [es:di], ax
	add		di, 2
	cmp		di, 0x3e
	jb		INT09_not_yet_9
	sub		di, 32
INT09_not_yet_9:
	mov		[es:0x1c], di
INT09_nobuff:
	mov		al, 0x20
	out		0x20, al
	pop		ds
	pop		di
	pop		es
	pop		bx
	pop		ax
    iret
