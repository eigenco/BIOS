INT10:
	cmp		ah, 0x00
	je		INT10_set_video_mode
	cmp		ah, 0x0e
	je		INT10_putchar
	mov		al, 0x1a
	mov		bx, 8
	iret
INT10_set_video_mode:
	push	ds
	push	si
	push	ax
	push	bx
	push	cx
	push	dx
	cmp		al, 0x0d
	jne		INT10_ah00_al13
	mov		si, VGA_REGS_0D
	call	INT10_load_VGA_regs
	mov		si, PALETTE_CGA
	call	INT10_load_palette
	jmp		INT10_ah00_end
INT10_ah00_al13:
	cmp		al, 0x13
	jne		INT10_ah00_al03
	mov		si, VGA_REGS_13
	call	INT10_load_VGA_regs
	jmp		INT10_ah00_end
INT10_ah00_al03:
	mov		si, VGA_REGS_03
	call	INT10_load_VGA_regs
	call	INT10_load_charset
	mov		si, PALETTE_EGA
	call	INT10_load_palette
	mov		di, 0xb800
	mov		es, di
	mov		di, 0
	mov		ax, 0x0700
	mov		cx, 4000
	rep		stosw
INT10_ah00_end:	
	pop		dx
	pop		cx
	pop		bx
	pop		ax
	pop		si
	pop		ds
	iret
INT10_load_VGA_regs:
	mov		bx, cs
	mov		ds, bx
	mov		dx, 0x3c2
	outsb
	mov		dx, 0x3c0
	mov		cx, 42
	rep		outsb
	mov		dx, 0x3c4
	mov		cx, 5
	rep		outsw
	mov		dx, 0x3ce
	mov		cx, 9
	rep		outsw
	mov		dx, 0x3d4
	mov		ax, 0x0011 ; write protect off
	out		dx, ax
	mov		cx, 25
	rep		outsw
	mov		dx, 0x3c0
	mov		al, 0x20
	out		dx, al
	mov		dx, 0x3da
	in		al, dx
	ret
INT10_load_palette:
	mov		cx, 64
	mov     bl, 0
INT10_palette:
	mov		dx, 0x3c8
	mov		al, bl
	out		dx, al
	inc		bl
	inc		dx
	outsb
	outsb
	outsb
	loop	INT10_palette
	ret
INT10_load_charset:
	mov		dx, 0x3ce
	mov		ax, 0x0005
	out		dx, ax	
	mov		dx, 0x3ce
	mov		ax, 0x0406
	out		dx, ax	
	mov		dx, 0x3c4
	mov		ax, 0x0402
	out		dx, ax	
	mov		dx, 0x3c4
	mov		ax, 0x0604
	out		dx, ax
	mov		si, FONT_VGA
	mov		di, 0xa000
	mov		es, di
	mov		di, 0
	mov		cx, 256*16
	rep		movsw
	mov		dx, 0x3c4
	mov		ax, 0x0302
	out		dx, ax
	mov		dx, 0x3c4
	mov		ax, 0x0204
	out		dx, ax
	mov		dx, 0x3ce
	mov		ax, 0x1005
	out		dx, ax
	mov		dx, 0x3ce
	mov		ax, 0x0e06
	out		dx, ax
	ret
INT10_putchar:
	push	ds
	push	si
	push	es
	push	di
	push	ax
	push	bx
	push	cx
	push	dx
	mov		bx, 0x40
	mov		es, bx
	mov		di, [es:0x50]
	shl		di, 1	
	cmp		al, 8 ; BACKSPACE
	jne		INT10_putchar_not_BS
	sub		di, 2
	mov		bx, 0xb800
	mov		es, bx
	mov		ax, 0x0720
	stosw
	sub		di, 2
	jmp		INT10_putchar_not_CHAR
INT10_putchar_not_BS:
	cmp		al, 13 ; CR
	jne		INT10_putchar_not_CR
	mov 	bl, al
	mov 	bh, 160
	mov 	ax, di
	div 	bh	
	shr 	ax, 8
	sub 	di, ax
	jmp		INT10_putchar_not_CHAR
INT10_putchar_not_CR:
	cmp		al, 10 ; LF
	jne		INT10_putchar_not_LF
	add		di, 160
	cmp		di, 2*80*25
	jb		INT10_putchar_continue_normally
	mov		dx, di
	mov		bx, 0xb800
	mov		ds, bx
	mov		es, bx
	mov		si, 160
	xor		di, di
	mov		cx, 80*25
	rep		movsw
	mov		di, dx
	sub		di, 160
INT10_putchar_continue_normally:
	jmp		INT10_putchar_not_CHAR	
INT10_putchar_not_LF:
	cmp		al, 32
	jb		INT10_putchar_not_CHAR
	cmp		di, 2*80*25
	jne		INT10_skip_scroll
	mov		dx, di
	mov		bx, 0xb800
	mov		ds, bx
	mov		es, bx
	mov		si, 160
	xor		di, di
	mov		cx, 80*25
	rep		movsw
	mov		di, dx
	sub		di, 160
INT10_skip_scroll:
	mov		bx, 0xb800
	mov		es, bx
	mov		ah, 7
	stosw
INT10_putchar_not_CHAR:
	mov		bx, 0x40
	mov		es, bx
	mov		bx, 0x50
	shr		di, 1
	mov		[es:bx], di
INT10_putchar_move_cursor:	
	mov		cx, di
	mov		al, 0x0e
	mov		dx, 0x3d4
	out		dx, al
	mov		al, ch
	mov		dx, 0x3d5
	out		dx, al
	mov		al, 0x0f
	mov		dx, 0x3d4
	out		dx, al
	mov		al, cl
	mov		dx, 0x3d5
	out		dx, al
	pop		dx
	pop		cx
	pop		bx
	pop		ax
	pop		di
	pop		es
	pop		si
	pop		ds
	iret

