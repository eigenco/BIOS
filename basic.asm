;;;; BIOS DISK SERVICE ;;;;

INT13:
	cmp     ah, 2
	je      INT13_read_disk
	cmp     ah, 3
	je      INT13_write_disk
	cmp     ah, 8
	je      INT13_disk_type
	iret
INT13_read_disk:
	push	ax
	push	bx
	push	cx
	push	dx
	push	di
	push	si
	
    mov     di, bx
    mov     si, ax

    mov     bx, cx
    and     bx, 63

    shr     cl, 6
    xchg    cl, ch

    xchg    dl, dh
    and     dx, 0x0f

    mov     al, dl
    or      al, 0xa0
    mov     dx, ATA6
    out     dx, al

    mov     ax, si
    mov     dx, ATA2
    out     dx, al

    mov     al, bl
    mov     dx, ATA3
    out     dx, al

    mov     al, cl
    mov     dx, ATA4
    out     dx, al

    mov     al, ch
    mov     dx, ATA5
    out     dx, al

    mov     al, 0x20
    mov     dx, ATA7
    out     dx, al

    mov     bx, si
INT13_keep_reading:
    mov     cx, 256
    mov     dx, ATA7
INT13_wait_ready:
    in      al, dx
    test    al, 8
    jz      INT13_wait_ready
INT13_read_word:
    mov     dx, ATA0
    in      ax, dx
    stosw
    loop    INT13_read_word
    dec     bl
    jnz     INT13_keep_reading
	
	pop		si
	pop     di
	pop     dx
	pop     cx
	pop		bx
	pop		ax
	iret

INT13_write_disk:
	push	ds
	push	ax
	push	bx
	push	cx
	push	dx
	push	di
	push	si

    mov     si, es
    mov     ds, si
    mov     si, bx
    mov     di, ax

    mov     bx, cx
    and     bx, 63

    shr     cl, 6
    xchg    cl, ch

    xchg    dl, dh
    and     dx, 0x0f

    mov     al, dl
    or      al, 0xa0
    mov     dx, ATA6
    out     dx, al

    mov     ax, di
    mov     dx, ATA2
    out     dx, al

    mov     al, bl
    mov     dx, ATA3
    out     dx, al

    mov     al, cl
    mov     dx, ATA4
    out     dx, al

    mov     al, ch
    mov     dx, ATA5
    out     dx, al

    mov     al, 0x30
    mov     dx, ATA7
    out     dx, al

    mov     bx, di
INT13_keep_writing:
    mov     dx, ATA7
    in      al, dx
    test    al, 0x80
    jnz     INT13_keep_writing
    mov     cx, 256
INT13_wait_ready_w:
    in      al, dx
    test    al, 8
    jz      INT13_wait_ready_w
INT13_write_word:
    mov     dx, ATA0
    lodsw
    out     dx, ax
    loop    INT13_write_word
    dec     bl
INT13_wait_ready_ww:
    mov     dx, ATA7
    in      al, dx
    test    al, 0x80
    jnz     INT13_wait_ready_ww
    mov     al, 0xe7
    mov     dx, ATA7
    out     dx, al
    jnz     INT13_keep_writing

	pop		si
	pop     di
	pop     dx
	pop     cx
	pop		bx
	pop		ax
	pop		ds
	iret

INT13_disk_type: ; 1024 cylinders, 16 heads, 63 sectors per cylinder
	mov     cx, 0xff3f
	mov     dx, 0x0f01
blank:
	iret

;;;; BIOS KEYBOARD SERVICE ;;;;

INT16:
	push	ds
	push	bx
	mov		bx, 0x40
	mov		ds, bx
	or		ah, ah
	jz		INT16_read
	dec		ah
	jz		INT16_wait
	xor		ax, ax
INT16_exit:
	pop		bx
	pop		ds
	iret
INT16_read:
	cli
	mov		bx, [ds:0x1a]
	cmp		bx, [ds:0x1c]
	sti
	jz		INT16_read
	mov		ax, [ds:bx]
	add		bx, 2
	cmp		bx, 0x3e
	jb		INT16_got_key
	sub		bx, 32
INT16_got_key:
	mov		[ds:0x1a], bx
	jmp		INT16_exit
INT16_wait:
	cli
	mov		bx, [ds:0x1a]
	cmp		bx, [ds:0x1c]
	mov		ax, [ds:bx]
	sti
	pop		bx
	pop		ds
	retf	2

;;;; BIOS SYSTEM CLOCK COUNTER SERVICE ;;;;

INT1A:
	push	ax
	push	ds
	mov		ax, 0x40
	mov		ds, ax
	mov		cx, [ds:0x6e]
	mov		dx, [ds:0x6c]
	pop		ds
	pop		ax
	iret

