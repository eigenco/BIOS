; 64k system BIOS at F000:0000 - F000:FFFF
;
; Boots to prompth with PCemV17 (ami386), disk with MS-DOS 6.22 installed
;
; BIOS interrupts are only partially implemented
;
; To find the correct HDD parameters (cx and dx) for ah = 8/int 13h
;
; debug
; a
; mov dl,80
; mov ah,8
; int 13
;
; g =100 106

	cpu	8086

start:

;;;; setup IBM-compatible stack ;;;;

	mov	sp, 30h
	mov	ss, sp
	mov	sp, 100h

;;;; setup VGA registers ;;;;

	mov	bx, cs
	mov	ds, bx

	mov	si, MISCregs
	mov	dx, 0x3c2
	lodsb
	out	dx, al

	mov	si, SEQregs
	mov	dx, 0x3c4
	mov	cx, 5
a:
	lodsw
	out	dx, ax
	loop	a

	mov	si, CRTCregs
	mov	dx, 0x3d4
	mov	cx, 26
b:
	lodsw
	out	dx, ax
	loop	b

	mov	si, GCregs
	mov	dx, 0x3ce
	mov	cx, 9
c:
	lodsw
	out	dx, ax
	loop	c

	mov	si, ACregs
	mov	dx, 0x3c0
	mov	cx, 42
d:
	lodsb
	out	dx, al
	loop	d

	mov	dx, 0x3da
	in	al, dx
	mov	dx, 0x3c0
	mov	al, 0x20
	out	dx, al

;;;; load palette ;;;;

	mov	si, PALETTE
	mov	cx, 16
pal:
	mov	dx, 0x3c8
	lodsw
	out	dx, ax
	inc	dx
	lodsb
	out	dx, al
	lodsb
	out	dx, al
	loop	pal

;;;; load font ;;;;

	mov	dx, 0x3ce
	mov	ax, 0x0005
	out	dx, ax
	mov	ax, 0x0406
	out	dx, ax
	mov	dx, 0x3c4
	mov	ax, 0x0402
	out	dx, ax
	mov	ax, 0x0604
	out	dx, ax
	mov	si, VGAFONT
	mov	bx, 0xa000
	mov	es, bx
	xor	di, di
	mov	cx, 80*25*2
	rep	movsw
	mov	ax, 0x0302
	out	dx, ax
	mov	ax, 0x0204
	out	dx, ax
	mov	dx, 0x3ce
	mov	ax, 0x1005
	out	dx, ax
	mov	ax, 0x0e06
	out	dx, ax

;;;; setup empty interrupt handlers ;;;;

	cli

	xor	bx, bx
	mov	es, bx
	mov	cx, 0x1f
empty:
	mov	bx, cx
	shl	bx, 1
	shl	bx, 1
	mov	word [es:bx], blank
	mov	word [es:bx+2], cs
	loop	empty

;;;; setup 8259 Programmable Interrupt Controller (PIC) ;;;;

	mov	al, 00010000b ; single 8259
	out	0x20, al

	mov	al, 00001000b
	out	0x21, al

	mov	al, 00000000b
	out	0x21, al

	mov	al, 00000000b ; all interrupts enabled
	out	0x21, al

;;;; setup 8253 Programmable Interval Timer (PIT) ;;;;

	mov	al, 00110110b ; counter 0, mode 3
	out	0x43, al

	mov	al, 0         ; 18.2 Hz
	out	0x40, al
	out	0x40, al

;;;; setup 8042 Keyboard Controller ;;;;

	mov	al, 0xF0      ; select scan code set
	out	0x60, al

	mov	al, 00000001b ; PC/XT
	out	0x60, al

;;;; setup interrupt handlers ;;;;

	mov     word [es:0x08*4+0], int8
	mov     word [es:0x08*4+2], cs
	mov     word [es:0x09*4+0], int9
	mov     word [es:0x09*4+2], cs
	mov     word [es:0x10*4+0], int10
	mov     word [es:0x10*4+2], cs
	mov     word [es:0x12*4+0], int12
	mov     word [es:0x12*4+2], cs
	mov     word [es:0x13*4+0], int13
	mov     word [es:0x13*4+2], cs
	mov     word [es:0x16*4+0], int16
	mov     word [es:0x16*4+2], cs

;;;; enable interrupts ;;;;

	sti

;;;; setup BIOS Data Area (BDA) and enable interrupts ;;;;

	mov	di, 0x40
	mov	es, di
	mov	word [es:0x1a], 0x1e

;;;; load and execute boot sector ;;;;

	xor	ax, ax
	mov	es, ax
	mov	ax, 0x0201
	mov	bx, 0x7c00
	mov	cx, 1
	mov	dx, 0x0080
	int	0x13
	jmp	0:0x7c00

;;;; PIT INTERRUPT ;;;;

int8:
	push	ax

	mov	al, 0x20
	out	0x20, al

	pop	ax

	iret

;;;; KEYBOARD INTERRUPT ;;;;

int9:
	push	ax
	push	es
	push	di

	in	al, 0x60
	cmp	al, 0x7f
	ja	int9_pass

	mov	di, 0x40
	mov	es, di
	inc	word [es:0x1a]
	mov	di, [es:0x1a]
	mov	[es:di], al
int9_pass:
	mov	al, 0x20
	out	0x20, al

	pop	di
	pop	es
	pop	ax

        iret

;;;; BIOS KEYBOARD INTERRUPT SERVICE ;;;;

int16:
	push	es
	push	di

        cmp     ah, 0
	je	kb_0
	cmp	ah, 1
	je	kb_1

	xor	ax, ax
	jmp	kb_end
  kb_1:
	cli
	mov	di, 0x40
	mov	es, di
	cmp	word [es:0x1a], 0x1e
	jne	kb_end
	sti

	xor	ax, ax
	jmp	kb_end
  kb_0:
	cli
	mov	di, 0x40
	mov	es, di
	cmp	word [es:0x1a], 0x1e
	jne	kb__0
	sti

	jmp	kb_0
  kb__0:
	mov	di, [es:0x1a]
	mov	al, [es:di]
	dec	word [es:0x1a]
  kb_end:
	pop	di
	pop	es
	sti
	retf	2

