int10:
	cmp	ah, 0x0e
	je	putchar
	iret
putchar:
	push	es
	push	di
	push	bx
	push	ax

	; load old cursor location from BIOS Data Area
	mov	bx, 0x40
	mov	es, bx
	mov	bx, 0x50
	mov	di, [es:bx]

	; write characters to VGA adapter
	mov	bx, 0xb800
	mov	es, bx
	mov	ah, 7
	stosw

	; store new cursor location to BIOS Data Area
	mov	bx, 0x40
	mov	es, bx
	mov	bx, 0x50
	mov	[es:bx], di

	pop	ax
	pop	bx
	pop	di
	pop	es
	iret

;;;; BIOS MEMORY INTERRUPT SERVICE ;;;;

int12:
	mov	ax, 640
	iret

;;;; BIOS DISK INTERRUPT SERVICE ;;;;

int13:
	cmp	ah, 2
	je	read_disk
	cmp	ah, 3
	je	write_disk
	cmp	ah, 8
	je	disk_type
	iret
read_disk:
	push	cx
	push	dx
	push	di

	mov	di, bx                 ; target = es:bx
	mov	bl, al                 ; sectors to read

	mov     al, dh                 ; head
	mov     dx, 0x1f6
	out     dx, al

	mov	al, bl                 ; sectors to read
	mov     dx, 0x1f2
	out     dx, al

	mov     al, cl                 ; sector
	mov     dx, 0x1f3
	out     dx, al

	mov     al, ch                 ; cylinder
	mov     dx, 0x1f4
	out     dx, al

	mov     al, 0x20               ; read (0x30 for write)
	mov     dx, 0x1f7
	out     dx, al
repeat:
	mov     dx, 0x1f7
processing:
	in      al, dx
	test    al, 8
	jz      processing
	mov     cx, 256
	mov     dx, 0x1f0
e:
	in	ax, dx
	stosw
	loop	e
	dec     bl
	jnz	repeat                 ; until sectors are read

	pop	di
	pop	dx
	pop	cx
	iret

write_disk:
	iret

disk_type:
	mov	cx, 0xFFD1
	mov	dx, 0x0E01
	iret

blank:
	iret

