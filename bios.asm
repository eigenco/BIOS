;;;; BIOS DISK SERVICE ;;;;

INT13:
	cmp     ah, 2
	je      read_disk
	cmp     ah, 3
	je      write_disk
	cmp     ah, 8
	je      disk_type
	iret
read_disk:
	push	ax
	push	bx
	push	cx
	push	dx
	push	di
	push	si
	
	mov     di, bx                     ; destination = es:bx
	mov		si, ax                     ; # of sectors to read
	
	;;;;;;;;
	
	mov		bx, cx
	and		bx, 63		               ; sector is now in bx
	
	shr		cl, 6
	xchg	cl, ch                     ; cylinder is now in cx
	
	mov		dl, dh
	and		dx, 0x0F                   ; head is now in dx

	; LBA = (cylinder * TOTAL_HEADS + head) * SECTORS_PER_CYL + sector - 1
	
	mov		ax, cx                     ; ax = cylinder
	shl     ax, 4                      ; ax = ax * 16
	add     ax, dx                     ; ax = ax + head
	mov     dx, 63
	mul     dx                         ; dx:ax = ax * 63
	dec     bx
	clc
	adc     ax, bx
	adc     dx, 0
	mov     bx, ax                     ; dx:ax = dx:ax + sector - 1	
	mov     cx, dx

	mov		al, ch                     ; al = LBA[27:24]
	or		al, 0xE0
	mov     dx, ATA+6
	out     dx, al	

	mov		ax, si                     ; # of sectors to read
	mov     dx, ATA+2
	out     dx, al
	
	mov		al, bl                     ; al = LBA[7:0]
	mov     dx, ATA+3
	out     dx, al

	mov		al, bh                     ; al = LBA[15:8]
	mov     dx, ATA+4
	out     dx, al
	
	mov     al, cl                     ; al = LBA[23:16]
	mov     dx, ATA+5
	out		dx, al
	
	;;;;;;;;
	
	mov     al, 0x20                   ; read
	mov		dx, ATA+7
	out     dx, al
	
	mov		bx, si                     ; # of sectors to read
r_sector_loop:
	in		al, dx
	test	al, 8
	jz		r_sector_loop
	and		dx, 0xFF0
	mov		cx, 256
r_word_loop:
	in		ax, dx
	stosw
	loop	r_word_loop
	or		dx, 7
	dec		bl
	jnz		r_sector_loop
	
	pop		si
	pop     di
	pop     dx
	pop     cx
	pop		bx
	pop		ax
	iret

write_disk:
	push	ds
	push	ax
	push	bx
	push	cx
	push	dx
	push	di
	push	si
	
	mov		si, es
	mov		ds, si
	mov     si, bx                     ; destination = es:bx
	mov		di, ax                     ; # of sectors to write

	;;;;;;;;
	
	mov		bx, cx
	and		bx, 63		               ; sector is now in bx
	
	shr		cl, 6
	xchg	cl, ch                     ; cylinder is now in cx
	
	mov		dl, dh
	and		dx, 0x0F                   ; head is now in dx

	mov		ax, cx                     ; ax = cylinder
	shl     ax, 4                      ; ax = ax * 16
	add     ax, dx                     ; ax = ax + head
	mov     dx, 63
	mul     dx                         ; dx:ax = ax * 63
	dec     bx
	clc
	adc     ax, bx
	adc     dx, 0
	mov     bx, ax                     ; dx:ax = dx:ax + sector - 1	
	mov     cx, dx

	mov		al, ch                     ; al = LBA[27:24]
	or		al, 0xE0
	mov     dx, ATA+6
	out     dx, al	

	mov		ax, di                     ; # of sectors to write
	mov     dx, ATA+2
