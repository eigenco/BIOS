; System BIOS at F000:0000 - F000:FFFF
; VGA BIOS not needed at C000:0000 - C000:7FFF
;
; What works (Tested: PCem, 386, VGA)
;   MS-DOS 6.22 (including edit)
;   Supaplex
;   Prince of persia
;   Keen 4
;   Eye of the Beholder 2
;   Space Quest 3
;   Wolfenstein 3D

%macro outb 2
	mov		dx, %1
	mov		al, %2
	out		dx, al
%endmacro

%macro outw 2
	mov		dx, %1
	mov		ax, %2
	out		dx, ax
%endmacro

%define ATA 0x1F0

	cpu     186

start:

;;;; SETUP INTERRUPT HANDLERS ;;;;

	cli
	xor     di, di
	mov     es, di
	mov     cx, 0x1F
blints:
	mov     ax, blank
	stosw
	mov     ax, cs
	stosw
	loop	blints
	mov     word [es:0x08*4], INT08 ; TIMER HANDLER
	mov     word [es:0x09*4], INT09 ; KEYBOARD HANDLER
	mov     word [es:0x10*4], INT10 ; VIDEO SERVICE
	mov     word [es:0x12*4], INT12 ; MEMORY SERVICE
	mov     word [es:0x13*4], INT13 ; DISK SERVICE
	mov     word [es:0x16*4], INT16 ; KEYBOARD SERVICE
	mov     word [es:0x1A*4], INT1A ; TIME SERVICE

;;;; BIOS DATA AREA ;;;;

	mov		di, 0x40
	mov		es, di
	mov		word [es:0x1A], 0x1E
	mov		word [es:0x1C], 0x1E
	
;;;; IBM COMPATIBLE STACK ;;;;

	mov		sp, 0x30
	mov		ss, sp
	mov		sp, 0x0100
	
;;;; INITIALIZE 8259 PROGRAMMABLE INTERRUPT CONTROLLER ;;;;

	mov		al, 16
	out		0x20, al
	mov		al, 8
	out		0x21, al
	xor		al, al	
	out		0x21, al
	xor		al, al
	out		0x21, al
	
;;;; INITIALIZE 8253 TIMER ;;;;

	mov		al, 0x36
	out		0x43, al
	xor		al, al   ; 18.2 Hz
	out		0x40, al
	out		0x40, al
	
;;;; INITIALIZE 8042 KEYBOARD CONTROLLER ;;;;

	mov     al, 0xF0
	out     0x60, al
	mov     al, 1
	out     0x60, al
	
;;;; BIOS DATA AREA ;;;;

	mov		di, 0x40
	mov		es, di
	mov		byte [es:0x49], 3  ; video mode
	mov		word [es:0x4A], 80 ; columns
	mov		word [es:0x50], 0  ; cursor position
	mov		byte [es:0x62], 0  ; page
	mov		byte [es:0x84], 24 ; rows - 1

;;;; INITIALIZE VIDEO TEXT MODE ;;;;

	mov		ax, 3
	int		0x10

;;;; LOAD BOOT SECTOR ;;;;
	
	xor		ax, ax
	mov		es, ax
	mov		ds, ax
	mov     ax, 0x0201
	mov     bx, 0x7C00
	mov     cx, 1
	mov     dx, 0x0080
	int     0x13
	
	jmp     0:0x7C00

;;;; VIDEO INTERRUPT HANDLER ;;;;

INT10:
	cmp		ah, 0x00
	je		INT10_SET_VIDEO_MODE
	cmp		ah, 0x02
	je		INT10_SET_CURSOR_POSITION
	cmp		ah, 0x06
	je		INT10_SCROLL_UP
	cmp		ah, 0x08
	je		INT10_READ_AT_CURSOR
	cmp		ah, 0x09
	je		INT10_WRITE_AT_CURSOR
	cmp		ah, 0x0E
	je		INT10_WRITE_CHARACTER
	cmp		ah, 0x0F
	je		INT10_GET_VIDEO_STATE
	cmp		ah, 0x12
	je		INT10_GET_VIDEO_CONFIGURATION1
	cmp		ah, 0x1A
	je		INT10_GET_VIDEO_CONFIGURATION2
	cmp		ah, 0x1B
	je		INT10_GET_VIDEO_CONFIGURATION3
	iret
INT10_SET_VIDEO_MODE:
	push	ds
	push	si
	push	ax
	push	bx
	push	cx
	push	dx
	cmp		al, 0x0D
	jne		AH00_AL13
	mov		si, VGA_REGS_0D
	call	load_VGA_regs
	mov		si, PALETTE_CGA
	call	load_palette
	jmp		AH00_END
AH00_AL13:
	cmp		al, 0x13
	jne		AH00_AL03
	mov		si, VGA_REGS_13
	call	load_VGA_regs
	jmp		AH00_END
AH00_AL03:
	mov		si, VGA_REGS_03
	call	load_VGA_regs
	call	load_charset
	mov		si, PALETTE_EGA
	call	load_palette
AH00_END:	
	pop		dx
	pop		cx
	pop		bx
	pop		ax
	pop		si
	pop		ds
	iret
load_VGA_regs:
	mov		bx, cs
	mov		ds, bx

	mov		dx, 0x3c2
	outsb

	mov		dx, 0x3c0
	mov		cx, 42
	rep		outsb

	mov		dx, 0x3c4
	mov		cx, 5
	rep		outsw

	mov		dx, 0x3ce
	mov		cx, 9
	rep		outsw

	mov		dx, 0x3d4
	mov		ax, 0x0011 ; write protect off
	out		dx, ax
	mov		cx, 25
	rep		outsw

	mov		dx, 0x3c0
	mov		al, 0x20
	out		dx, al
	
	mov		dx, 0x3da
	in		al, dx
	ret
load_palette:
	mov		cx, 64
	mov     bl, 0
palette:
	mov		dx, 0x3c8
	mov		al, bl
	out		dx, al
	inc		bl
	inc		dx
	outsb
	outsb
	outsb
	loop	palette
	ret
load_charset:
	outw	0x3ce, 0x0005
	outw	0x3ce, 0x0406
	outw	0x3c4, 0x0402
	outw	0x3c4, 0x0604
	mov		si, FONT_VGA
	mov		di, 0xa000
	mov		es, di
	mov		di, 0
	mov		cx, 256*16
	rep		movsw
	outw	0x3c4, 0x0302
	outw	0x3c4, 0x0204
	outw	0x3ce, 0x1005
	outw	0x3ce, 0x0e06
	ret
INT10_SET_CURSOR_POSITION:
	push	ds
	push	ax
	push	bx
	push	cx
	push	dx
	
	mov		ax, 0x40
	mov		ds, ax
	mov		[ds:0x50], dx ; update BDA
	
	mov		ah, 0
	mov     al, dh
	mov		bl, 80
	mul		bl	
	mov		dh, 0	
	add		ax, dx
	mov		cx, ax
	
	mov		al, 0x0F
	mov		dx, 0x3D4
	out		dx, al
	mov		al, cl
	inc		dx
	out		dx, al
	mov		al, 0x0E
	mov		dx, 0x3D4
	out		dx, al
	mov		al, ch
	inc		dx
	out		dx, al
	pop		dx
	pop		cx
	pop		bx
	pop		ax
	pop		ds
	iret
INT10_SCROLL_UP:
	push	es
	push	di
	push	ax
	push	bx
	push	cx
	push	dx
	cmp		al, 0	
	jne		skipAH06
	mov		bx, 0xB800
	mov		es, bx
	mov		di, 0
	mov		cx, 80*25
	mov		ax, 0x0720
	rep		stosw
	mov		ah, 2
	mov		dx, 0
	int		0x10
skipAH06:
	pop		dx
	pop		cx
	pop		bx
	pop		ax
	pop		di
	pop		es
	iret
INT10_READ_AT_CURSOR:
	mov		ah, 7
	iret
INT10_WRITE_AT_CURSOR:
	push	es
	push	di
	push	ax
	push	bx
	push	cx
	push	dx
	
	push	cx

	mov		di, 0x40
	mov		es, di
	mov		dx, [es:0x50] ; load old cursor location

	mov		bh, bl
	mov		bl, al
	mov		al, dh
	mov		ah, 0
	mov		cl, 160
	mul		cl
	mov		dh, 0
	shl		dl, 1
	add		ax, dx
	mov		di, ax
	mov		al, bl

	pop		cx
	mov		dx, 0xB800
	mov		es, dx
	mov		ah, bh
	rep		stosw

	pop		dx
	pop		cx
	pop		bx
	pop		ax
	pop		di
	pop		es

	iret
INT10_WRITE_CHARACTER:
	push	ds
	push	si
	push	es
	push	di
	push	ax
	push	bx
	push	cx
	push	dx
	
	mov		di, 0x40
	mov		es, di
	mov		dx, [es:0x50] ; load old cursor location

	mov		bl, al
	mov		al, dh
	mov		ah, 0         ; ax = row
	mov		cl, 160
	mul		cl            ; ax = 160*ax
	mov		dh, 0
	shl		dl, 1
	add		ax, dx        ; ax = ax + 2*column
	mov		di, ax	      ; di = 160*row + 2*column
	mov		al, bl

	cmp		al, 8 ; BACKSPACE
	jne		notBS
	sub		di, 2
	mov		bx, 0xB800
	mov		es, bx
	mov		ax, 0x0720
	stosw
	sub		di, 2
	jmp		nonCHAR
notBS:
	cmp		al, 13 ; CR
	jne		notCR
	mov 	bl, al
	mov 	bh, 160
	mov 	ax, di
	div 	bh	
	shr 	ax, 8
	sub 	di, ax
	jmp		nonCHAR
notCR:
	cmp		al, 10 ; LF
	jne		notLF
	add		di, 160
	cmp		di, 2*80*25
	jb		continue_normally ; scroll if necessary
	mov		dx, di
	mov		bx, 0xB800
	mov		ds, bx
	mov		es, bx
	mov		si, 160
	xor		di, di
	mov		cx, 80*25
	rep		movsw
	mov		di, dx
	sub		di, 160
continue_normally:
	jmp		nonCHAR	
notLF:
	cmp		al, 32
	jb		nonCHAR
	mov		bx, 0xB800
	mov		es, bx
	mov		ah, 7
	stosw ; write characters to video memory
nonCHAR:
	mov		bx, 0x40
	mov		es, bx
	mov		bx, 0x50
	
	mov		ax, di ; calculate the new cursor dx
	mov		cx, 160
	div		cl
	shr		ah, 1
	xchg	al, ah
	
	mov		dx, ax
	mov		ah, 2	
	int		0x10 ; update cursor location

	pop		dx
	pop		cx
	pop		bx
	pop		ax
	pop		di
	pop		es
	pop		si
	pop		ds
	iret
INT10_GET_VIDEO_STATE:
	mov		ah, 80
	mov		al, 3
	mov		bh, 0
	iret
INT10_GET_VIDEO_CONFIGURATION1:
	mov		bx, 3
	iret
INT10_GET_VIDEO_CONFIGURATION2:
	mov		al, 0x1A
	mov		bx, 8
	iret
INT10_GET_VIDEO_CONFIGURATION3:
	mov		al, 0x1B
	mov		bx, cs
	mov		es, bx
	mov		di, VTABLE
	iret
VTABLE:
	dw		0xF000
	dw		STABLE
	db		3         ; video mode
STABLE:
	db      00001000b ; supported video modes 7:0 (3)
	db      00100000b ; supported video modes F:8 (D)
	db      00001000b ; supported video modes 13:10 (13)

;;;; TIMER INTERRUPT HANDLER ;;;;

INT08:
	push	ds
	push	ax
	mov		ax, 0x40
	mov		ds, ax
	clc
	adc		word [ds:0x6C], 1
	adc		word [ds:0x6E], 0
	int		0x1C
	mov		al, 0x20
	out		0x20, al
	pop		ax
	pop		ds
	iret

;;;; BIOS CONVENTIONAL MEMORY SERVICE ;;;;

INT12:
	mov		ax, 640
	iret

;;;; BIOS SYSTEM CLOCK COUNTER SERVICE ;;;;

INT1A:
	push	ax
	push	ds
	mov		ax, 0x40
	mov		ds, ax
	mov		cx, [ds:0x6E]
	mov		dx, [ds:0x6C]
	pop		ds
	pop		ax
	iret

;;;; KEYBOARD INTERRUPT HANDLER ;;;;

INT09:
	push	ax
	push	bx
	push	es
	push	di
	push	ds
	
	in      al, 0x60
	
	mov		di, 0x40
	mov		es, di		
	mov		di, [es:0x1C]

	cmp		al, 0x1D                   ; <CTRL> down
	jne		not_1D
	or      [es:0x17], byte 00000100b	
	jmp		nobuff
not_1D:

	cmp		al, 0x9D                   ; <CTRL> up
	jne		not_9D
	and     [es:0x17], byte 11111011b	
	jmp		nobuff
not_9D:

	cmp		al, 0x38                   ; <ALT> down
	jne		not_38
	or      [es:0x17], byte 00001000b	
	jmp		nobuff
not_38:

	cmp		al, 0xB8                   ; <ALT> up
	jne		not_B8
	and     [es:0x17], byte 11110111b
	jmp		nobuff
not_B8:

	cmp		al, 0x2A                   ; <LSHIFT> down
	jne		not_2A
	or      [es:0x17], byte 00000010b
	jmp		nobuff
not_2A:

	cmp		al, 0xAA                   ; <LSHIFT> up
	jne		not_AA
	and     [es:0x17], byte 11111101b
	jmp		nobuff
not_AA:

	cmp		al, 0x36                   ; <RSHIFT> down
	jne		not_36
	or      [es:0x17], byte 00000001b	
	jmp		nobuff
not_36:

	cmp		al, 0xB6                   ; <RSHIFT> up
	jne		not_B6
	and     [es:0x17], byte 11111110b	
	jmp		nobuff
not_B6:	

	cmp		al, 0x7F
	ja		nobuff
	
	mov		bx, cs
	mov		ds, bx
	mov		ah, al
	mov		bx, ASCII
	xlatb

	mov		bl, [es:0x17]
	test	bl, 3
	jz		not_SHIFT
	sub		al, 0x20
not_SHIFT:

	mov		bl, [es:0x17]
	test	bl, 4
	jz		not_CTRL
	sub		al, 0x60
not_CTRL:

	mov		bl, [es:0x17]
	test	bl, 8
	jz		not_ALT
	xor		al, al
not_ALT:

	mov		word [es:di], ax
	add		di, 2
	cmp		di, 0x3E
	jb		not_yet_9
	sub		di, 32
not_yet_9:
	mov		[es:0x1C], di
nobuff:
	mov     al, 1
	out     0x64, al
	mov		al, 0x20
	out		0x20, al
	
	pop		ds
	pop		di
	pop		es
	pop		bx
	pop		ax
    iret

;;;; BIOS KEYBOARD SERVICE ;;;;

INT16:
	push	ds
	push	bx
	mov		bx, 0x40
	mov		ds, bx
	or		ah, ah
	jz		KBD_read
	dec		ah
	jz		KBD_wait
	xor		ax, ax
KBD_exit:
	pop		bx
	pop		ds
	iret
KBD_read:
	cli
	mov		bx, [ds:0x1A]
	cmp		bx, [ds:0x1C]
	jnz		KBD_rea
	sti
	jmp		KBD_read
KBD_rea:	
	mov		ax, [ds:bx]
	add		bx, 2
	cmp		bx, 0x3E
	jb		KBD_not_yet
	sub		bx, 32
KBD_not_yet:
	mov		[ds:0x1A], bx
	jmp		KBD_exit
KBD_wait:
	cli
	mov		bx, [ds:0x1A]       ; if [40:1A] != [40:1C]
	cmp		bx, [ds:0x1C]       ; there is a key waiting in the buffer
	mov		ax, [ds:bx]
	sti
	pop		bx
	pop		ds
	retf	2

